// Operadores Aritiméticos

// Multipllicação (*)
console.log(3 * 5)
console.log(3.7 * 5.9)

// Divisão (/)
console.log(12 / 2)

// Soma (+)
console.log(5 + 2)

// Subtração (-)
console.log(5 - 2)

// Resto da divisão (%)
console.log(5 % 2)

// incremento (++)
let increment = 0
increment++
console.log(increment)


// decremento (--)
let decrement = 5
decrement--
console.log(decrement)


// exponencial (**)
console.log(3 ** 3)
