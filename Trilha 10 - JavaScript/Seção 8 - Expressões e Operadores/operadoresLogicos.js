// Operadores Lógicos

// São 2 valores booleanos sendo verificados, quando verificados, retornam verdadeirou ou falso

let pao = true
let queijo = false

// AND (&&) -- Os dois valores precisam ser verdadeiros para retornar true
console.log(pao && queijo)

// OR (||) -- Apenas um dos valores precisa ser verdadeiros para retornar true
console.log(pao || queijo)


// NOT (!) - inberte o valor bolleano 
console.log(!pao)