// Operadores de comparação

// Esses operadores comparam valores e retornam um Boolena como resposta à comparação
let one = 1
let two = 2

// Igual a (==), compara apenas o valor e não o tipo
console.log(two == 1)
console.log(one == "1")



// Diferente de (!=)

let one = 1
let two = 2

console.log(one != two)
console.log(one != 1 )
console.log(one != "1")

// Estritamente igual a (===) compara o valor e tipo
console.log(one === "1")
console.log(one === 1)

// Estritamente diferente (!==) compara o valor e tipo
console.log(one !== "1")
console.log(one !== 1)


// Maior que (>)
console.log(one < two)

// Menor que (<)
console.log(one > two)

// Menor igual a (<=)
console.log(one <= two)

// Maior igual a (=>)
console.log(one >= two)